type GetPokemonResponse struct {
	Count    int         `json:"count"`
	Next     string      `json:"next"`
	Previous interface{} `json:"previous"`
	Results  []Pokemon   `json:"results"`
}

func main() {
	fmt.Println("Hello World")

	http.HandleFunc(("/"), func(w http.ResponseWriter, r *http.Request) {
		tmpl := template.Must(template.ParseFiles("index.html", "header.html", "fragment/pokemon_list.html"))
		resp, _ := http.Get("https://pokeapi.co/api/v2/pokemon")
		body, _ := ioutil.ReadAll(resp.Body)
		var pokemonResponse GetPokemonResponse
		json.Unmarshal(body, &pokemonResponse)

		var pokemon []PokemonWithImage
		for _, p := range pokemonResponse.Results {

			var id = strings.Split(p.URL, "/")[6]
			var image = "https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/" + id + ".png"
			pokemon = append(pokemon, PokemonWithImage{
				Name:  p.Name,
				URL:   p.URL,
				Image: image,
			})
		}

		context := map[string]interface{}{
			"pokemons": pokemon,
		}
		tmpl.Execute(w, context)
	})

	// Pokemon Detail
	http.HandleFunc(("/pokemon/"), func(w http.ResponseWriter, r *http.Request) {
		tmpl := template.Must(template.ParseFiles("detail.html", "header.html"))
		pokemonName := strings.Split(r.URL.Path, "/")[2]
		resp, _ := http.Get("https://pokeapi.co/api/v2/pokemon/" + pokemonName)
		body, _ := ioutil.ReadAll(resp.Body)
		var pokemonDetail PokemonDetail
		json.Unmarshal(body, &pokemonDetail)

		context := map[string]interface{}{
			"pokemon": pokemonDetail,
		}
		tmpl.Execute(w, context)
	})

	// Search
	http.HandleFunc(("/action/search"), func(w http.ResponseWriter, r *http.Request) {
		resp, _ := http.Get("https://pokeapi.co/api/v2/pokemon")
		body, _ := ioutil.ReadAll(resp.Body)
		var pokemonResponse GetPokemonResponse
		json.Unmarshal(body, &pokemonResponse)

		var pokemon []PokemonWithImage
		for _, p := range pokemonResponse.Results {

			var id = strings.Split(p.URL, "/")[6]
			var image = "https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/" + id + ".png"
			pokemon = append(pokemon, PokemonWithImage{
				Name:  p.Name,
				URL:   p.URL,
				Image: image,
			})
		}

		var search = r.URL.Query().Get("query")
		var filteredPokemon []PokemonWithImage
		for _, p := range pokemon {
			if strings.Contains(p.Name, search) {
				filteredPokemon = append(filteredPokemon, p)
			}
		}

		// delay

		tmpl := template.Must(template.New("search").Parse(`
		{{ range .}}
			{{ template "pokemon_list" .}}
		{{ end }}
		`))

		tmpl.ParseFiles("fragment/pokemon_list.html")

		tmpl.Execute(w, filteredPokemon)

	})

	log.Fatal(http.ListenAndServe(":3000", nil))
}
